import unittest
from src.utils.types import FileType

class FileTypeTestCase(unittest.TestCase):
    def test_priority(self):
        self.assertEqual(FileType.SOURCE_CODE.priority, 0.5)
        self.assertEqual(FileType.DOCS.priority, 1.0)
        self.assertEqual(FileType.CONFIGURATION.priority, 0.8)
        self.assertEqual(FileType.OTHER.priority, 0.1)

    def test_from_extension(self):
        self.assertEqual(FileType.from_extension('.md'), FileType.DOCS)
        self.assertEqual(FileType.from_extension('.txt'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('.pdf'), FileType.DOCS)
        self.assertEqual(FileType.from_extension('.yaml'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('.yml'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('.json'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('.xml'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('.py'), FileType.SOURCE_CODE)
        self.assertEqual(FileType.from_extension('.js'), FileType.SOURCE_CODE)
        self.assertEqual(FileType.from_extension('.java'), FileType.SOURCE_CODE)
        self.assertEqual(FileType.from_extension('.cpp'), FileType.SOURCE_CODE)
        self.assertEqual(FileType.from_extension('.c'), FileType.SOURCE_CODE)
        self.assertEqual(FileType.from_extension('.go'), FileType.SOURCE_CODE)
        self.assertEqual(FileType.from_extension('.rb'), FileType.SOURCE_CODE)
        self.assertEqual(FileType.from_extension('.php'), FileType.SOURCE_CODE)
        self.assertEqual(FileType.from_extension('.tsx'), FileType.SOURCE_CODE)
        self.assertEqual(FileType.from_extension('.ts'), FileType.SOURCE_CODE)
        self.assertEqual(FileType.from_extension('.html'), FileType.SOURCE_CODE)
        self.assertEqual(FileType.from_extension('.css'), FileType.SOURCE_CODE)
        self.assertEqual(FileType.from_extension('.sh'), FileType.SOURCE_CODE)
        self.assertEqual(FileType.from_extension('.bat'), FileType.SOURCE_CODE)
        self.assertEqual(FileType.from_extension('.csproj'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('.sln'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('.cs'), FileType.SOURCE_CODE)
        self.assertEqual(FileType.from_extension('.cjs'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('.unknown'), FileType.OTHER)
        self.assertEqual(FileType.from_extension('.env'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('.ini'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('.cfg'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('.conf'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('.log'), FileType.OTHER)
        self.assertEqual(FileType.from_extension('dockerfile'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('makefile'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('procfile'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('jenkinsfile'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('.env'), FileType.CONFIGURATION)
        self.assertEqual(FileType.from_extension('license'), FileType.OTHER)